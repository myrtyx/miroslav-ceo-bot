def build_memory_context() -> str:
    if not CHAT_MEMORY_PATH.exists():
        return ""
    try:
        text = CHAT_MEMORY_PATH.read_text(encoding="utf-8").strip()
        if not text:
            return ""
        return f"\n\nПАМЯТЬ О ПРОШЛЫХ РАЗГОВОРАХ:\n{text}"
    except Exception:
        return ""


